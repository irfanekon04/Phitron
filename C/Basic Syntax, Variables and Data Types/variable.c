#include<stdio.h>

int  main()
{
    int a = 10;
    printf("%d",a); // %d is used to print decimal integers
    
    float b = 6.9;
    printf("\n%f",b);// %f is used to print floating point numbers
    //  \n is used for new line after printing the value of 'a'
    char c='A';
    printf("\n%c",c); // %c is used to print characters
    // In C, we use single quotes(' ') to represent character literals

    return 0;
}